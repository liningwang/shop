package com.cn.hnust.pojo;

public class MyProduct {
	String shopPictureUrl;
	String shopTitle;
	Double shopPrice;
	Integer shopNumber;
	Integer shopId;
	Integer type;
	Integer cancelState;
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getCancelState() {
		return cancelState;
	}
	public void setCancelState(Integer cancelState) {
		this.cancelState = cancelState;
	}
	public String getShopPictureUrl() {
		return shopPictureUrl;
	}
	public void setShopPictureUrl(String shopPictureUrl) {
		this.shopPictureUrl = shopPictureUrl;
	}
	public String getShopTitle() {
		return shopTitle;
	}
	public void setShopTitle(String shopTitle) {
		this.shopTitle = shopTitle;
	}
	public Double getShopPrice() {
		return shopPrice;
	}
	public void setShopPrice(Double shopPrice) {
		this.shopPrice = shopPrice;
	}
	public Integer getShopNumber() {
		return shopNumber;
	}
	public void setShopNumber(Integer shopNumber) {
		this.shopNumber = shopNumber;
	}
	public Integer getShopId() {
		return shopId;
	}
	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}
	
}
